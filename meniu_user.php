  <li>Cum sesizați o faptă de corupție?</li><br>
  <h5> Aveți posibilitatea să contribuiți la semnalarea unor fapte de corupție despre care aveți cunoștință și la tragerea la răspundere a persoanelor suspectate de comiterea lor. În acest scop, aveți la dispoziție formularul din stanga, pe care il puteți completa.</h5><br>
  <li>Cum este protejat cel care denunță?</li><br>
   <h5> Mituitorul sau cumpărătorul de influență nu se pedepsește dacă denunță autorității fapta, mai înainte ca organul de urmărire să fi fost sesizat pentru acea infracțiune. Banii, valorile sau orice alte bunuri se restituie persoanei care le-a dat, în acest caz (art. 255 alin. 3 și 5 din Codul penal, respectiv art. 6¹ alin 2 și 4 din Legea 78/2000).</h5><br>
  <li>Fazele procesului penal</li><br>
   <h5> Procesul penal este o activitate ce urmărește constatarea la timp și în mod concret a faptelor ce constituie infracțiuni, astfel încât persoana care a săvârșit o infracțiune să fie trasă la raspundere penală.

Procesul penal are trei faze:
<ul>
<li> Urmărirea penală; are ca obiect strângerea probelor pentru trimiterea în judecată a inculpatului. Această activitate este efectuată de organele de cercetare penală și de către procuror. </li>
<li> Judecata; începe o dată cu sesizarea instanței și continuă până la rămânerea definitivă a hotărârii de condamnare. Această activitate este efectuată de judecător. </li>
<li> Punerea în executare a hotărârilor penale de condamnare rămase definitive.</li>
</ul>
